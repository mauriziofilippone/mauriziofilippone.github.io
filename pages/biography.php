<b>Short Biography</b>
<p>
Maurizio Filippone received a Master&#39;s degree in Physics and a Ph.D. in Computer Science from the University of Genova, Italy, in 2004 and 2008, respectively. 
</p>

<p>
In 2007, during his Ph.D. studies, he visited George Mason University, Fairfax, VA as a Research Scholar for about eight months.
From 2008 to 2011, he was a Research Associate with the University of Sheffield, U.K. (2008-2009), with the University of Glasgow, U.K. (2010), and with University College London, U.K (2011).
In 2011, he took up a Lecturer position at the University of Glasgow, U.K, which he left in 2015 to join EURECOM, Sophia Antipolis, France as an Associate Professor.
In 2024 he joined the Statistics Program at KAUST as an Associate Professor.
</p>

<p>
  His current research interests include the development of tractable and scalable Bayesian inference techniques for Gaussian processes and Deep Learning models with applications in life and environmental sciences.
</p>

